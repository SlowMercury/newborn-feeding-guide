 // Helper functions
export interface Module {
  name: string;
  order: number;
  lessons: Lesson[];
}

export function getModules(): Module[] {
  const moduleMap = new Map<string, Module>();

  for (const lesson of lessons) {
    if (!moduleMap.has(lesson.module)) {
      moduleMap.set(lesson.module, {
        name: lesson.module,
        order: lesson.moduleOrder,
        lessons: [],
      });
    }
    moduleMap.get(lesson.module)!.lessons.push(lesson);
  }

  const modules = Array.from(moduleMap.values());
  modules.sort((a, b) => a.order - b.order);
  modules.forEach((m) => m.lessons.sort((a, b) => a.order - b.order));

  return modules;
}

export function getLessonById(id: string): Lesson | undefined {
  return lessons.find((l) => l.id === id);
}

export function getOrderedLessons(): Lesson[] {
  return getModules().flatMap((m) => m.lessons);
}

export function getAdjacentLessons(id: string): { prev: Lesson | null; next: Lesson | null } {
  const ordered = getOrderedLessons();
  const idx = ordered.findIndex((l) => l.id === id);
  return {
    prev: idx > 0 ? ordered[idx - 1] : null,
    next: idx < ordered.length - 1 ? ordered[idx + 1] : null,
  };
}

// --- Search helpers ---

function extractBlockText(block: LessonBlock): string {
  switch (block.type) {
    case "heading":
    case "paragraph":
    case "quote":
      return block.text;
    case "list":
      return block.items.join(" ");
    case "table":
      return [...block.headers, ...block.rows.flat()].join(" ");
    case "callout":
      return `${block.title} ${block.text}`;
    default:
      return "";
  }
}

export function getLessonPlainText(lesson: Lesson): string {
  return lesson.content.map(extractBlockText).join(" ");
}

export interface SearchResult {
  lesson: Lesson;
  snippet: string;
  matchStart: number;
  matchEnd: number;
}

export function searchLessons(query: string): SearchResult[] {
  if (!query || query.trim().length < 2) return [];

  const normalizedQuery = query.toLowerCase().trim();
  const results: SearchResult[] = [];

  for (const lesson of lessons) {
    const fullText = `${lesson.title} ${getLessonPlainText(lesson)}`;
    const lowerText = fullText.toLowerCase();
    const matchIndex = lowerText.indexOf(normalizedQuery);

    if (matchIndex !== -1) {
      // Build a snippet around the match
      const snippetStart = Math.max(0, matchIndex - 40);
      const snippetEnd = Math.min(fullText.length, matchIndex + normalizedQuery.length + 60);
      let snippet = fullText.slice(snippetStart, snippetEnd).trim();
      if (snippetStart > 0) snippet = "…" + snippet;
      if (snippetEnd < fullText.length) snippet = snippet + "…";

      results.push({
        lesson,
        snippet,
        matchStart: matchIndex - snippetStart + (snippetStart > 0 ? 1 : 0),
        matchEnd: matchIndex - snippetStart + normalizedQuery.length + (snippetStart > 0 ? 1 : 0),
      });
    }
  }

  return results;
}
